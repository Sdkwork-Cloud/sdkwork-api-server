export function translatePortalZhCnFallback(text: string): string | undefined {
  if (!text || /^[A-Z0-9_/-]+$/.test(text) || /^https?:\/\//.test(text) || text.startsWith('/')) {
    return undefined;
  }

  let translated = text;

  for (const [needle, replacement] of PHRASE_REPLACEMENTS) {
    translated = translated.replaceAll(needle, replacement);
  }

  return translated === text ? undefined : translated;
}
